DRAW_BACKBOARD PROC FAR
	ASSUME DS:DATA, SS:STACK, CS:CODE
	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	PUSH BP
	
	SUB BX,BX
	
	MOV BP,SP
	
	SUB AX,AX
	SUB BX,BX
	SUB CX,CX
	SUB DX,DX
	
	MOV BX,[BP]+14
	MOV DX,[BP]+16
	MOV CX,[BP]+18
	MOV AX,[BP]+20

	CMP AX,0
	JZ BACK2
	
	INC BX
BACK1:
	MOV AH,0CH
	;White line
	MOV AL,04
	INT 10H
	INC DX
	CMP DX,BX
	JNZ BACK1
	JMP NEXT
	
BACK2:
	MOV AH,0CH
	;White line
	MOV AL,03
	INT 10H
	INC CX
	CMP CX,BX
	JNZ BACK2

NEXT:	
	POP BP
	POP DX
	POP CX
	POP BX
	POP AX
	
	RET 8
DRAW_BACKBOARD ENDP
CODE ENDS
END